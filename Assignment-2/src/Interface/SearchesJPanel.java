    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        
        
        String brand = txtBrandName.getText();
        String year = txtManufacturingYear.getText();
        String min = txtMinimum.getText();
        String max = txtMaximum.getText();
        String model = txtModelNumber.getText();
        String city = txtCity.getText(); 
        
        
        if(brand.isEmpty() && year.isEmpty() && model.isEmpty() && city.isEmpty() )
        {
        JOptionPane.showMessageDialog(null,"Kindly provide some Search Criteria!");
        }
        else{
            UpdateJPanel panel = new UpdateJPanel(userProcessContainerJPanel,carDirectory,brand,year,min,max,model,city);
            userProcessContainerJPanel.add("UpdateJPanel",panel);
            CardLayout layout = (CardLayout) userProcessContainerJPanel.getLayout();
            layout.next(userProcessContainerJPanel);
        }
        
        
    }//GEN-LAST:event_btnSearchActionPerformed

