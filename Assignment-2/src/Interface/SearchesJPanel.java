    private void btnCarBySeatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCarBySeatActionPerformed
        // TODO add your handling code here:
        int min = Integer.parseInt(txtMinimum.getText());
        int max = Integer.parseInt(txtMaximum.getText());
        
        Car result = carDirectory.MinMaxSeats(min, max);
        if(result == null){
            JOptionPane.showMessageDialog(null, "No such match found for the Seat Capacity specified!");
        }

        else{
            
            
                UpdateJPanel viewpanel = new UpdateJPanel(userProcessContainerJPanel, carDirectory, min, max);
                userProcessContainerJPanel.add("UpdateJPanel",viewpanel);
                CardLayout layout = (CardLayout) userProcessContainerJPanel.getLayout();
                layout.next(userProcessContainerJPanel);
        }
        
        
        
    }//GEN-LAST:event_btnCarBySeatActionPerformed

